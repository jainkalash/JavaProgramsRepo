package com.kalash.IfElse;

public class IfStatementRunner {

	public static void main(String[] args) {
		int i = 25;
		if (i == 25) {
			System.out.println("i is equal to 25");
		} else if (i == 24) {
			System.out.println("i is equal to 24");
		} else {
			System.out.println("i is not equal to 24 and 25");
		}
	}

}
